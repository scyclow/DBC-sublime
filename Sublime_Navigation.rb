SUBLIME NAVIGATION
__________________

ACTION              											 KEYSTROKE
======																		===========
*Search Bar                							command + p

*Switch Tabs 														command + option + arrow(l/r)
	
Select Tab 															command + [1..0]
	
Select Quadrant 												control + [1,2,3,4]
	
Move File to Quadrant										control + shift + [1,2,3,4]
	
Cycle Through Recent Tabs								control + tab
Cycle Backwards Through Recent Tabs			control + shift + tab